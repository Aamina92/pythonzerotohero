# Assigning different types of values to variables

name = "Tuba"            # String variable
age = 2					 # Integer variable
height = 1.1             # Float variable
is_student = False       # Boolean variable

# printing the variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is student:", is_student)

# Performing operations using variables
new_age = age + 1 # incremeneting age by 1
print("Next year age will be :", new_age)

